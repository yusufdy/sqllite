package com.example.sqllite;

public class PersonBean {
    String name;
    int age;
    public PersonBean() {

    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }
}
